package cj.day1;

public class SumAndAverageOfThreeNumbers {

    public static void main(String[] args) {

        float num1 = 10.5f;
        float num2 = 20.5f;
        float num3 = 30.5f;

        float sum = num1 + num2 + num3;
        float avg = sum / 3;

        System.out.println("Sum of three numbers is     :" +sum);
        System.out.println("Average of three numbers is :" +avg);

    }
}
